def calcular_score(row):

    score = 0

    # =====================================================
    # P/VP
    # =====================================================

    if row["PVP"] < 0.95:
        score += 3

    elif row["PVP"] < 1:
        score += 2

    # =====================================================
    # DY
    # =====================================================

    if row["DY"] >= 12:
        score += 3

    elif row["DY"] >= 9:
        score += 2

    # =====================================================
    # LIQUIDEZ
    # =====================================================

    if row["Liquidez"] >= 10000000:
        score += 3

    elif row["Liquidez"] >= 1000000:
        score += 2

    return score
